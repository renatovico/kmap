"""Export CircuitGraph to synthesisable HDL (Verilog / VHDL).

Emits the optimized gate DAG as structural RTL that maps directly
to FPGA LUT primitives.  Every node in the graph becomes a module
instantiation:

- **CONST** → wire assignment (constant bit pattern)
- **Arithmetic** (add, sub, mul, div) → IEEE-754 FPU modules (instantiate
  vendor float IP, or behavioral for simulation)
- **LUT** (silu, exp, rsqrt) → ROM / block-RAM lookup table
- **Reductions** (sum, max_reduce) → reduction tree
- **Wiring** (reshape, transpose, slice) → wire reordering (zero logic)

Pipeline registers are inserted at subgraph boundaries (configurable
pipeline depth).

Target platforms:
- Xilinx Alveo / Zynq (LUT6): uses BRAM18/36 for activation LUTs
- Intel/Altera Stratix / Cyclone (ALM): uses M20K for activation LUTs
- Lattice iCE40 via Yosys/nextpnr: uses SPRAM for activation LUTs

Usage::

    from kllm.hdl_export import export_verilog, export_vhdl
    export_verilog(graph, "output/circuit.v")
    export_vhdl(graph, "output/circuit.vhd")
"""

from __future__ import annotations

import os
from datetime import datetime

import numpy as np

from kllm.circuit_graph import CircuitGraph, Node, Op


class _StreamWriter:
    """Drop-in replacement for ``list`` that streams lines to a file.

    Helper functions use ``_L = lines.append``; by passing a
    _StreamWriter instead of a real list, every line goes straight
    to disk — no 4 GB string in memory.
    """

    def __init__(self, f) -> None:
        self._f = f

    def append(self, line: str) -> None:          # noqa: D401
        self._f.write(line)
        self._f.write("\n")


# ---------------------------------------------------------------
# Verilog emitter
# ---------------------------------------------------------------

def export_verilog(
    graph: CircuitGraph,
    path: str,
    module_name: str = "circuit_top",
    pipeline_depth: int = 0,
    float_width: int = 32,
) -> str:
    """Export a CircuitGraph to synthesisable Verilog.

    Parameters
    ----------
    graph : CircuitGraph
        The circuit to export (typically after optimize_graph).
    path : str
        Output .v file path.
    module_name : str
        Top-level module name.
    pipeline_depth : int
        Number of pipeline register stages to insert.
    float_width : int
        Bit width per float element (32 for float32).

    Returns
    -------
    str
        Output file path.
    """
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    _file = open(path, "w")
    lines = _StreamWriter(_file)
    _L = lines.append

    # Analyse graph
    inputs = [n for n in graph.nodes if n.op == Op.INPUT]
    consts = [n for n in graph.nodes if n.op == Op.CONST]
    outputs = _find_output_nodes(graph)

    _L(f"// Auto-generated by kllm hdl_export")
    _L(f"// Date: {datetime.now().isoformat()}")
    _L(f"// Nodes: {len(graph)}, Pipeline: {pipeline_depth}")
    _L(f"")
    _L(f"module {module_name} (")
    _L(f"    input  wire clk,")
    _L(f"    input  wire rst_n,")
    _L(f"    input  wire valid_in,")
    _L(f"    output wire valid_out,")

    # Input ports
    for i, node in enumerate(inputs):
        size = _tensor_bits(node, float_width)
        _L(f"    input  wire [{size - 1}:0] in_{node.id},  // {node.name}")

    # Output ports
    for i, node in enumerate(outputs):
        size = _tensor_bits(node, float_width)
        comma = "" if i == len(outputs) - 1 else ","
        _L(f"    output wire [{size - 1}:0] out_{node.id}{comma}  // {node.name}")

    _L(f");")
    _L(f"")

    # Constant wires
    for node in consts:
        size = _tensor_bits(node, float_width)
        hex_val = _const_to_hex(node, float_width)
        _L(f"    // {node.name or f'const_{node.id}'}")
        _L(f"    wire [{size - 1}:0] n_{node.id} = {size}'h{hex_val};")

    _L(f"")

    # Input wire aliases
    for node in inputs:
        size = _tensor_bits(node, float_width)
        _L(f"    wire [{size - 1}:0] n_{node.id} = in_{node.id};")

    _L(f"")

    # Process nodes in topological order
    order = graph.topological_order()
    pipeline_stage = 0
    nodes_per_stage = max(1, len(order) // max(pipeline_depth, 1))

    for idx, nid in enumerate(order):
        node = graph.nodes[nid]
        if node.op in (Op.CONST, Op.INPUT):
            continue

        # Pipeline register insertion
        if pipeline_depth > 0 and idx > 0 and idx % nodes_per_stage == 0:
            pipeline_stage += 1
            _L(f"")
            _L(f"    // --- Pipeline stage {pipeline_stage} ---")
            _L(f"    reg [{float_width - 1}:0] pipe_{pipeline_stage}_valid;")
            _L(f"    always @(posedge clk or negedge rst_n)")
            _L(f"        if (!rst_n) pipe_{pipeline_stage}_valid <= 0;")
            _L(f"        else pipe_{pipeline_stage}_valid <= 1;")

        _emit_node_verilog(lines, node, graph, float_width)

    _L(f"")

    # Output assignments
    for node in outputs:
        _L(f"    assign out_{node.id} = n_{node.id};")

    # Valid pipeline
    if pipeline_depth > 0:
        _L(f"    assign valid_out = pipe_{pipeline_stage}_valid;")
    else:
        _L(f"    assign valid_out = valid_in;")

    _L(f"")
    _L(f"endmodule")
    _L(f"")

    # Emit helper modules
    _emit_helper_modules(lines, graph, float_width)

    _file.close()
    return path


