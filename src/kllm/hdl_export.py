def export_vhdl(
    graph: CircuitGraph,
    path: str,
    entity_name: str = "circuit_top",
    float_width: int = 32,
) -> str:
    """Export a CircuitGraph to synthesisable VHDL.

    Parameters
    ----------
    graph : CircuitGraph
        The circuit to export.
    path : str
        Output .vhd file path.
    entity_name : str
        Top-level entity name.
    float_width : int
        Bit width per float element.

    Returns
    -------
    str
        Output file path.
    """
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    _file = open(path, "w")
    lines = _StreamWriter(_file)
    _L = lines.append

    inputs = [n for n in graph.nodes if n.op == Op.INPUT]
    consts = [n for n in graph.nodes if n.op == Op.CONST]
    outputs = _find_output_nodes(graph)

    _L(f"-- Auto-generated by kllm hdl_export")
    _L(f"-- Date: {datetime.now().isoformat()}")
    _L(f"-- Nodes: {len(graph)}")
    _L(f"")
    _L(f"library IEEE;")
    _L(f"use IEEE.std_logic_1164.all;")
    _L(f"use IEEE.numeric_std.all;")
    _L(f"")
    _L(f"entity {entity_name} is")
    _L(f"    port (")
    _L(f"        clk      : in  std_logic;")
    _L(f"        rst_n    : in  std_logic;")
    _L(f"        valid_in : in  std_logic;")
    _L(f"        valid_out: out std_logic;")

    for node in inputs:
        size = _tensor_bits(node, float_width)
        _L(f"        in_{node.id} : in  std_logic_vector({size - 1} downto 0);  -- {node.name}")

    for i, node in enumerate(outputs):
        size = _tensor_bits(node, float_width)
        semi = "" if i == len(outputs) - 1 else ";"
        _L(f"        out_{node.id}: out std_logic_vector({size - 1} downto 0){semi}  -- {node.name}")

    _L(f"    );")
    _L(f"end entity {entity_name};")
    _L(f"")
    _L(f"architecture rtl of {entity_name} is")
    _L(f"")

    # Signal declarations
    for node in graph.nodes:
        if node.op in (Op.CONST, Op.INPUT):
            continue
        size = _tensor_bits(node, float_width)
        _L(f"    signal n_{node.id} : std_logic_vector({size - 1} downto 0);  -- {node.op.value}: {node.name}")

    for node in consts:
        size = _tensor_bits(node, float_width)
        _L(f"    constant n_{node.id} : std_logic_vector({size - 1} downto 0) := x\"{_const_to_hex(node, float_width)}\";")

    _L(f"")
    _L(f"begin")
    _L(f"")

    # Input aliases
    for node in inputs:
        _L(f"    n_{node.id} <= in_{node.id} after 1 ns;  -- {node.name}")

    _L(f"")

    # Process nodes
    for nid in graph.topological_order():
        node = graph.nodes[nid]
        if node.op in (Op.CONST, Op.INPUT):
            continue
        _emit_node_vhdl(lines, node, graph, float_width)

    _L(f"")

    # Output assignments
    for node in outputs:
        _L(f"    out_{node.id} <= n_{node.id};")

    _L(f"    valid_out <= valid_in;")
    _L(f"")
    _L(f"end architecture rtl;")

    _file.close()
    return path


