def export_testbench(
    graph: CircuitGraph,
    path: str,
    values: dict[int, np.ndarray],
    module_name: str = "circuit_top",
    float_width: int = 32,
) -> str:
    """Generate a SystemVerilog testbench with golden values.

    Uses pre-computed node values (from the reference evaluator)
    as golden checks.

    Parameters
    ----------
    graph : CircuitGraph
        The circuit under test.
    path : str
        Output testbench file path.
    values : dict
        Node values from evaluate() — used as golden reference.
    module_name : str
        Name of the DUT module.
    float_width : int
        Bit width per float element.

    Returns
    -------
    str
        The generated testbench source code.
    """
    lines: list[str] = []
    _L = lines.append

    inputs = [n for n in graph.nodes if n.op == Op.INPUT]
    outputs = _find_output_nodes(graph)

    _L(f"// Auto-generated testbench for {module_name}")
    _L(f"`timescale 1ns / 1ps")
    _L(f"")
    _L(f"module tb_{module_name};")
    _L(f"    reg clk = 0;")
    _L(f"    reg rst_n = 0;")
    _L(f"    reg valid_in = 0;")
    _L(f"    wire valid_out;")

    for node in inputs:
        size = _tensor_bits(node, float_width)
        _L(f"    reg [{size - 1}:0] in_{node.id};")

    for node in outputs:
        size = _tensor_bits(node, float_width)
        _L(f"    wire [{size - 1}:0] out_{node.id};")

    _L(f"")
    _L(f"    // DUT instantiation")
    _L(f"    {module_name} dut (")
    _L(f"        .clk(clk), .rst_n(rst_n),")
    _L(f"        .valid_in(valid_in), .valid_out(valid_out),")
    for node in inputs:
        _L(f"        .in_{node.id}(in_{node.id}),")
    for i, node in enumerate(outputs):
        comma = "" if i == len(outputs) - 1 else ","
        _L(f"        .out_{node.id}(out_{node.id}){comma}")
    _L(f"    );")
    _L(f"")
    _L(f"    // Clock generation")
    _L(f"    always #5 clk = ~clk;")
    _L(f"")
    _L(f"    initial begin")
    _L(f"        rst_n = 0;")
    _L(f"        #20 rst_n = 1;")
    _L(f"        #10;")

    # Drive inputs
    for node in inputs:
        if node.id in values:
            hex_val = _array_to_hex(values[node.id], float_width)
            _L(f"        in_{node.id} = {_tensor_bits(node, float_width)}'h{hex_val};")

    _L(f"        valid_in = 1;")
    _L(f"        #10;")

    # Check outputs (combinational — results available after propagation)
    for node in outputs:
        if node.id in values:
            size = _tensor_bits(node, float_width)
            hex_val = _array_to_hex(values[node.id], float_width)
            _L(f"        // Check {node.name}")
            _L(f"        if (out_{node.id} !== {size}'h{hex_val})")
            _L(f"            $error(\"MISMATCH: out_{node.id} expected={size}'h{hex_val} got=%h\", out_{node.id});")
            _L(f"        else")
            _L(f"            $display(\"PASS: out_{node.id}\");")

    _L(f"")
    _L(f"        $display(\"All checks completed.\");")
    _L(f"        $finish;")
    _L(f"    end")
    _L(f"endmodule")

    source = "\n".join(lines)
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    with open(path, "w") as f:
        f.write(source)

    return source


